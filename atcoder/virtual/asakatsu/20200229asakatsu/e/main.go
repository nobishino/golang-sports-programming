func readVariables() {
	N = nextInt()
	for i := 0; i < N; i++ {
		for j := 0; j < N; j++ {
			weight := nextInt()
			if i >= j {
				continue
			}
			Links = append(Links, Link{i, j, weight})
		}
	}
	sort.Sort(Links)
}

type LList []Link

func (l LList) Len() int           { return len(l) }
func (l LList) Less(i, j int) bool { return l[i].weight < l[j].weight }
func (l LList) Swap(i, j int)      { l[i], l[j] = l[j], l[i] }

