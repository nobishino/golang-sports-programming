package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

var (
	N      int
	s      string
	answer int
	w, r   []int
)

const red = 'R'
const white = 'W'

func main() {
	readVariables()
	answer = N
	for i := 0; i <= N; i++ {
		cand := MaxInt(w[i], r[i])
		answer = MinInt(cand, answer)
	}
	fmt.Println(answer)
}

func readVariables() {
	N = nextInt()
	s = nextStr()
	w, r = make([]int, N+1), make([]int, N+1)
	for i := 1; i <= N; i++ {
		w[i] = w[i-1]
		if i-1 < N && s[i-1] == white {
			w[i]++
		}
	}
	for i := N; i >= 0; i-- {
		if i+1 < N+1 {
			r[i] = r[i+1]
		}
		if i < N && s[i] == red {
			r[i]++
		}
	}
}

